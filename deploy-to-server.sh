    else
        echo -e "${RED}‚ùå –°–µ—Ä–≤–∏—Å Xpanel –Ω–µ –∑–∞–ø—É—â–µ–Ω${NC}"
        echo -e "${YELLOW}–õ–æ–≥–∏ —Å–µ—Ä–≤–∏—Å–∞:${NC}"
        ssh ${SERVER_USER}@${SERVER_IP} "journalctl -u xpanel -n 10 --no-pager"
        exit 1
    fi
    
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º Nginx
    if ssh ${SERVER_USER}@${SERVER_IP} "systemctl is-active --quiet nginx"; then
        echo -e "${GREEN}‚úÖ Nginx –∑–∞–ø—É—â–µ–Ω${NC}"
    else
        echo -e "${RED}‚ùå Nginx –Ω–µ –∑–∞–ø—É—â–µ–Ω${NC}"

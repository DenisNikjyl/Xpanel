# –°–æ–∑–¥–∞–Ω–∏–µ –∞—Ä—Ö–∏–≤–∞ –ø—Ä–æ–µ–∫—Ç–∞
create_project_archive() {
    echo -e "${YELLOW}üì¶ –°–æ–∑–¥–∞–Ω–∏–µ –∞—Ä—Ö–∏–≤–∞ –ø—Ä–æ–µ–∫—Ç–∞...${NC}"
    
    # –ò—Å–∫–ª—é—á–∞–µ–º –Ω–µ–Ω—É–∂–Ω—ã–µ —Ñ–∞–π–ª—ã –∏ –ø–∞–ø–∫–∏
    tar --exclude='node_modules' \
        --exclude='client/node_modules' \
        --exclude='client/build' \
        --exclude='.git' \
        --exclude='*.log' \
        --exclude='*.tmp' \
        --exclude='deploy-to-server.sh' \
        -czf ${PROJECT_NAME}.tar.gz .
    
    echo -e "${GREEN}‚úÖ –ê—Ä—Ö–∏–≤ —Å–æ–∑–¥–∞–Ω: ${PROJECT_NAME}.tar.gz${NC}"
}

# –ó–∞–≥—Ä—É–∑–∫–∞ –Ω–∞ —Å–µ—Ä–≤–µ—Ä
upload_to_server() {
    echo -e "${YELLOW}‚¨ÜÔ∏è  –ó–∞–≥—Ä—É–∑–∫–∞ –Ω–∞ —Å–µ—Ä–≤–µ—Ä...${NC}"
    

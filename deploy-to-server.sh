    # –ó–∞–≥—Ä—É–∂–∞–µ–º —Å–∫—Ä–∏–ø—Ç —Å–±–æ—Ä–∫–∏
    scp build-linux.sh ${SERVER_USER}@${SERVER_IP}:/tmp/
    
    echo -e "${GREEN}‚úÖ –§–∞–π–ª—ã –∑–∞–≥—Ä—É–∂–µ–Ω—ã –Ω–∞ —Å–µ—Ä–≤–µ—Ä${NC}"
}

